package SeleniumJava.SeleniumJava;

public class String {

	public static void main(java.lang.String[] args) {
		java.lang.String name= "Arulprasath";
		System.out.println(name);
		System.out.println(name.charAt(0));
		System.out.println(name.contains("Arul"));
		System.out.println(name.concat("Agni"));
		System.out.println(name.equalsIgnoreCase("ARULPRASATH"));
		System.out.println(name.equals("Arulprasath"));
		System.out.println(name.hashCode());
		System.out.println(name.replace("Arul", "Agni"));
		System.out.println(name.toLowerCase());
		System.out.println(name.toUpperCase());
		System.out.println(name.trim());
		System.out.println(name.isEmpty());
		System.out.println(name.length());
		System.out.println(name.substring(3));
		System.out.println(name.substring(0, 3));
		

	}

}
